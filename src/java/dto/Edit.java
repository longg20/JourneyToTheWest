package dto;

import java.io.Serializable;

/**
 *
 * @author Long
 */
public class Edit implements Serializable {
    String id;
    String time;

    public Edit() {
    }

    public Edit(String id, String date) {
        this.id = id;
        this.time = date;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
